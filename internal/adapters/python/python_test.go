// Adapter integration test
// ---------------------------------------------------------------------------

func TestAdapterLoad(t *testing.T) {
	dir := t.TempDir()
	requirements := `requests==2.31.0
flask==3.0.0
`
	if err := os.WriteFile(filepath.Join(dir, "requirements.txt"), []byte(requirements), 0o600); err != nil {
		t.Fatal(err)
	}

	a := &Adapter{}
	if a.Name() != "python" {
		t.Errorf("Name(): got %q, want %q", a.Name(), "python")
	}

	g, err := a.Load(dir)
	if err != nil {
		t.Fatalf("Load: %v", err)
	}
	if g == nil {
		t.Fatal("Load returned nil graph")
	}

	// Should have root module + at least the two deps.
	if len(g.Modules) < 2 {
		t.Errorf("expected at least 2 modules, got %d", len(g.Modules))
	}

	if _, ok := g.Packages["requests"]; !ok {
		t.Error("expected 'requests' package in graph")
	}
	if _, ok := g.Packages["flask"]; !ok {
		t.Error("expected 'flask' package in graph")
	}
}

