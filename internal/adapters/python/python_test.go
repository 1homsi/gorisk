func FuzzParseRequirementsTxt(f *testing.F) {
	f.Add([]byte("requests==2.31.0\nflask>=2.0\n"))
	f.Add([]byte(""))
	f.Add([]byte("# comment only"))
	f.Add([]byte("-r other.txt\n--index-url https://example.com\n"))

	f.Fuzz(func(t *testing.T, data []byte) {
		defer func() { recover() }() //nolint:errcheck

		dir := t.TempDir()
		if err := os.WriteFile(filepath.Join(dir, "requirements.txt"), data, 0o600); err != nil {
			return
		}
		loadRequirementsTxt(dir) //nolint:errcheck
	})
}

