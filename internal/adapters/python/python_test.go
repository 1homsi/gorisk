func TestDetectCapabilities(t *testing.T) {
	dir := t.TempDir()
	src := `import subprocess
import os
import requests

def run_cmd(cmd):
    subprocess.run(cmd, shell=True)

def fetch(url):
    return requests.get(url)

def read_env():
    return os.getenv("SECRET_KEY")
`
	if err := os.WriteFile(filepath.Join(dir, "main.py"), []byte(src), 0o600); err != nil {
		t.Fatal(err)
	}

	caps := Detect(dir)

	wantCaps := []string{"exec", "network", "env"}
	for _, want := range wantCaps {
		if !caps.Has(want) {
			t.Errorf("expected capability %q to be detected", want)
		}
	}
}

func TestDetectNoCapabilities(t *testing.T) {
	dir := t.TempDir()
	src := `def add(a, b):
    return a + b

def greet(name):
    return f"Hello, {name}"
`
	if err := os.WriteFile(filepath.Join(dir, "utils.py"), []byte(src), 0o600); err != nil {
		t.Fatal(err)
	}

	caps := Detect(dir)
	if !caps.IsEmpty() {
		t.Errorf("expected no capabilities for benign code, got: %v", caps.List())
	}
}

