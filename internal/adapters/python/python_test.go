func TestLoadRequirementsTxt(t *testing.T) {
	dir := t.TempDir()
	content := `# project dependencies
requests==2.31.0
flask>=2.0
# dev tool
pytest
-r other-requirements.txt
`
	if err := os.WriteFile(filepath.Join(dir, "requirements.txt"), []byte(content), 0o600); err != nil {
		t.Fatal(err)
	}

	pkgs, err := loadRequirementsTxt(dir)
	if err != nil {
		t.Fatalf("loadRequirementsTxt: %v", err)
	}

	byName := make(map[string]PythonPackage)
	for _, p := range pkgs {
		byName[p.Name] = p
	}

	if _, ok := byName["requests"]; !ok {
		t.Error("expected 'requests' in packages")
	}
	if byName["requests"].Version != "2.31.0" {
		t.Errorf("requests version: got %q, want %q", byName["requests"].Version, "2.31.0")
	}
	if _, ok := byName["flask"]; !ok {
		t.Error("expected 'flask' in packages")
	}
	if _, ok := byName["pytest"]; !ok {
		t.Error("expected 'pytest' in packages")
	}
	// The -r option should not be parsed as a package.
	if _, ok := byName["-r"]; ok {
		t.Error("option line '-r' should not be a package")
	}
}

func TestLoadPoetryLock(t *testing.T) {
	dir := t.TempDir()
	lockContent := `# This file is automatically @generated by Poetry.
# It is not intended for manual editing.

[[package]]
name = "requests"
version = "2.31.0"
description = "Python HTTP for Humans."
optional = false
python-versions = ">=3.7"

[package.dependencies]
charset-normalizer = ">=2,<4"
idna = ">=2.5,<4"
urllib3 = ">=1.21.1,<3"
certifi = ">=2017.4.22"

[[package]]
name = "urllib3"
version = "2.0.4"
description = "HTTP library with thread-safe connection pooling."
optional = false
python-versions = ">=3.7"
`
	pyproject := `[tool.poetry]
name = "my-project"
version = "0.1.0"

[tool.poetry.dependencies]
python = ">=3.9"
requests = "^2.31.0"
`
	if err := os.WriteFile(filepath.Join(dir, "poetry.lock"), []byte(lockContent), 0o600); err != nil {
		t.Fatal(err)
	}
	if err := os.WriteFile(filepath.Join(dir, "pyproject.toml"), []byte(pyproject), 0o600); err != nil {
		t.Fatal(err)
	}

	pkgs, err := loadPoetryLock(dir)
	if err != nil {
		t.Fatalf("loadPoetryLock: %v", err)
	}

	byName := make(map[string]PythonPackage)
	for _, p := range pkgs {
		byName[p.Name] = p
	}

	if _, ok := byName["requests"]; !ok {
		t.Error("expected 'requests'")
	}
	if byName["requests"].Version != "2.31.0" {
		t.Errorf("requests version: got %q", byName["requests"].Version)
	}
	if !byName["requests"].Direct {
		t.Error("requests should be a direct dep")
	}
	// urllib3 is a transitive dep.
	if byName["urllib3"].Direct {
		t.Error("urllib3 should not be a direct dep")
	}
	// requests should have urllib3 as a dependency.
	found := false
	for _, dep := range byName["requests"].Dependencies {
		if dep == "urllib3" {
			found = true
		}
	}
	if !found {
		t.Error("requests should depend on urllib3")
	}
}

