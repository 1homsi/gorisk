func TestLoadCargoLockTruncated(t *testing.T) {
	// Truncated mid-package — should not panic.
	dir := t.TempDir()
	content := `version = 3

[[package]]
name = "serde"
version = "1.0`
	if err := os.WriteFile(filepath.Join(dir, "Cargo.lock"), []byte(content), 0o600); err != nil {
		t.Fatal(err)
	}
	// The test itself serves as a panic detector.
	pkgs, err := loadCargoLock(dir)
	if err != nil {
		t.Fatalf("loadCargoLock() unexpected error for truncated file: %v", err)
	}
	// Partial parsing: serde may or may not appear (version is malformed),
	// but no panic must occur.
	_ = pkgs
}

