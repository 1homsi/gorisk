func TestAdapterLoad(t *testing.T) {
	dir := t.TempDir()
	content := `[package]
name = "myapp"
version = "0.1.0"

[dependencies]
serde = "1.0"
reqwest = "0.11"
`
	if err := os.WriteFile(filepath.Join(dir, "Cargo.toml"), []byte(content), 0o600); err != nil {
		t.Fatal(err)
	}

	a := &Adapter{}
	if a.Name() != "rust" {
		t.Errorf("Name(): got %q, want %q", a.Name(), "rust")
	}

	g, err := a.Load(dir)
	if err != nil {
		t.Fatalf("Load: %v", err)
	}
	if g == nil {
		t.Fatal("Load returned nil graph")
	}

	// Should have root module + at least the two deps.
	if len(g.Modules) < 2 {
		t.Errorf("expected at least 2 modules, got %d", len(g.Modules))
	}

	if _, ok := g.Packages["serde"]; !ok {
		t.Error("expected 'serde' package in graph")
	}
	if _, ok := g.Packages["reqwest"]; !ok {
		t.Error("expected 'reqwest' package in graph")
	}
}

