func FuzzParseCargoLock(f *testing.F) {
	f.Add([]byte("[[package]]\nname = \"serde\"\nversion = \"1.0.193\"\n"))
	f.Add([]byte(""))
	f.Add([]byte("# comment only"))
	f.Add([]byte("version = 3\n[[package]]\nname = \"foo\"\nversion = \"0.1.0\"\ndependencies = [\"bar\"]\n"))

	f.Fuzz(func(t *testing.T, data []byte) {
		defer func() { recover() }() //nolint:errcheck

		dir := t.TempDir()
		if err := os.WriteFile(filepath.Join(dir, "Cargo.lock"), data, 0o600); err != nil {
			return
		}
		loadCargoLock(dir) //nolint:errcheck
	})
}

