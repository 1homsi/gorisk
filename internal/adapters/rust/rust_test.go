func TestLoadCargoLock(t *testing.T) {
	dir := t.TempDir()

	// Write a minimal Cargo.toml so we know the root name.
	cargoToml := `[package]
name = "myapp"
version = "0.1.0"

[dependencies]
serde = "1.0"
reqwest = "0.11"
`
	if err := os.WriteFile(filepath.Join(dir, "Cargo.toml"), []byte(cargoToml), 0o600); err != nil {
		t.Fatal(err)
	}

	lockContent := `# This file is automatically @generated by Cargo.
# It is not intended for manual editing.
version = 3

[[package]]
name = "myapp"
version = "0.1.0"
dependencies = [
  "serde",
  "reqwest",
]

[[package]]
name = "serde"
version = "1.0.193"
source = "registry+https://github.com/rust-lang/crates.io-index"

[[package]]
name = "reqwest"
version = "0.11.23"
source = "registry+https://github.com/rust-lang/crates.io-index"
dependencies = [
  "serde",
]
`
	if err := os.WriteFile(filepath.Join(dir, "Cargo.lock"), []byte(lockContent), 0o600); err != nil {
		t.Fatal(err)
	}

	pkgs, err := loadCargoLock(dir)
	if err != nil {
		t.Fatalf("loadCargoLock: %v", err)
	}

	byName := make(map[string]RustPackage)
	for _, p := range pkgs {
		byName[p.Name] = p
	}

	if _, ok := byName["serde"]; !ok {
		t.Error("expected 'serde' in packages")
	}
	if byName["serde"].Version != "1.0.193" {
		t.Errorf("serde version: got %q, want %q", byName["serde"].Version, "1.0.193")
	}
	if _, ok := byName["reqwest"]; !ok {
		t.Error("expected 'reqwest' in packages")
	}
	if byName["reqwest"].Version != "0.11.23" {
		t.Errorf("reqwest version: got %q, want %q", byName["reqwest"].Version, "0.11.23")
	}
	// Root package should be filtered out.
	if _, ok := byName["myapp"]; ok {
		t.Error("root package 'myapp' should be filtered out")
	}
	// reqwest should depend on serde.
	found := false
	for _, dep := range byName["reqwest"].Dependencies {
		if dep == "serde" {
			found = true
		}
	}
	if !found {
		t.Error("reqwest should depend on serde")
	}
}

