func TestDetectCapabilities(t *testing.T) {
	dir := t.TempDir()
	src := `use std::process::Command;
use std::fs;
use reqwest;

fn main() {
    let output = Command::new("ls").output().unwrap();
    let key = std::env::var("SECRET_KEY").unwrap();
    let data = std::fs::read("file.txt").unwrap();
    unsafe {
        // raw pointer operations
    }
}
`
	if err := os.WriteFile(filepath.Join(dir, "main.rs"), []byte(src), 0o600); err != nil {
		t.Fatal(err)
	}

	caps := Detect(dir)

	wantCaps := []string{"exec", "env", "fs:read", "fs:write", "network", "unsafe"}
	for _, want := range wantCaps {
		if !caps.Has(want) {
			t.Errorf("expected capability %q to be detected", want)
		}
	}
}

func TestDetectNoCapabilities(t *testing.T) {
	dir := t.TempDir()
	src := `fn add(a: i32, b: i32) -> i32 {
    a + b
}

fn greet(name: &str) -> String {
    format!("Hello, {}!", name)
}
`
	if err := os.WriteFile(filepath.Join(dir, "lib.rs"), []byte(src), 0o600); err != nil {
		t.Fatal(err)
	}

	caps := Detect(dir)
	if !caps.IsEmpty() {
		t.Errorf("expected no capabilities for benign code, got: %v", caps.List())
	}
}

